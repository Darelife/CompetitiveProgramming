class Solution {
public:
  int findWinningPlayer(vector<int>& skills, int k) {
    int n = skills.size();
    int max = 0;
    map<int, int> result;
    for (int i = 0; i < n - 1; i++) {
      if (skills[i] > max) {
        if (i > 1) result[i]++;
        max = skills[i];
        result[i]++;
        if (k <= result[i]) return i;
      }
    }
    return n - 1;
  }
};